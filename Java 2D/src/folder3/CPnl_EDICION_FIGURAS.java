    private void vCBox_OPERACIONES_BOOLEANASActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_vCBox_OPERACIONES_BOOLEANASActionPerformed
    {//GEN-HEADEREND:event_vCBox_OPERACIONES_BOOLEANASActionPerformed
        if (vCBox_OPERACIONES_BOOLEANAS.getSelectedIndex() != 0)
        {
            Area a = new Area();
            boolean b = false;
            boolean c = false;
            for (int i = 0; i < JavaDraw2DPanel.getShapes().size(); i++)
            {
                if (JavaDraw2DPanel.getShapes().get(i).isSeleccion() && !b)
                {
                    a = new Area(JavaDraw2DPanel.getShapes().get(i).getFigura());
                    b = true;
                    JavaDraw2DPanel.getShapes().remove(i);
                    i--;
                } else if (JavaDraw2DPanel.getShapes().get(i).isSeleccion() && b)
                {
                    Area aAux = new Area(JavaDraw2DPanel.getShapes().get(i).getFigura());
                    switch (vCBox_OPERACIONES_BOOLEANAS.getSelectedIndex())
                    {
                        case Cnfg.CONFIG_INT_OPERACION_BOOLEANA_UNION:
                            a.add(aAux);
                            break;
                        case Cnfg.CONFIG_INT_OPERACION_BOOLEANA_INTERSECCION:
                            a.intersect(aAux);
                            break;
                        case Cnfg.CONFIG_INT_OPERACION_BOOLEANA_SUSTRACCION:
                            a.subtract(aAux);
                            break;
                        case Cnfg.CONFIG_INT_OPERACION_BOOLEANA_XOR:
                            a.exclusiveOr(aAux);
                            break;
                    }
                    JavaDraw2DPanel.getShapes().remove(i);
                    c = true;
                    i--;
                }
            }
            if (c)
            {
                Shape newShape = new Area(a);
                JavaDraw2DPanel.getShapes().add(new CMisFiguras(newShape));
            } else
            {
                System.out.println("Porfavor seleccione minimo de dos figuras, osea no mamen.");
            }
            System.out.println("Figuras en el arreglo = " + JavaDraw2DPanel.getShapes().size());
        }
    }//GEN-LAST:event_vCBox_OPERACIONES_BOOLEANASActionPerformed


