    /**
     * Este metodo recibe una figura la cual contiene todas las configuraciones
     * de los paneles
     *
     */
    public void actualizarPanel()
    {
        //Primero cargo la configuracion del contorno
        //  1.- Tipo del contorno (Solido o Gradiente)
        getRdBtnSolido_CONTORNO().setSelected(false);
        getRdBtnGradiente_CONTORNO().setSelected(false);
        if (Cnfg.CONFIG_COLOR_CONTORNO_2 != null)
        {
            getRdBtnGradiente_CONTORNO().setSelected(true);
        } else
        {
            getRdBtnSolido_CONTORNO().setSelected(true);
            Cnfg.CONFIG_COLOR_CONTORNO_2 = null;
        }
        getvPnlMuestraColor().paintComponent(getvPnlMuestraColor().getGraphics());
        vSpinner_ESPACIADO.setValue(Cnfg.CONFIG_FLOAT_ESPACIO);
        vSpinner_CONTORNO_TAMI.setValue(Cnfg.CONFIG_FLOAT_LINEADO);
        vSpinner_CONTORNO_ANCHO.setValue(Cnfg.CONFIG_FLOAT_ANCHO);
        //Fin de la configuracion del contorno

        //Segundo, configuracion del relleno
        rdBtnSolido_RELLENO.setSelected(false);
        rdBtnGradiente_RELLENO.setSelected(false);
        //rdBtnTextura_RELLENO.setSelected(false);
        if (Cnfg.CONFIG_BUFFERED_IMAGE_RELLENO_3 != null)
        {
            //rdBtnTextura_RELLENO.setSelected(true);
        } else if (Cnfg.CONFIG_COLOR_RELLENO_2 != null)
        {
            rdBtnGradiente_RELLENO.setSelected(true);
        } else
        {
            rdBtnSolido_RELLENO.setSelected(true);
        }
        vPnlMuestraColor_RELLENO.paintComponent(vPnlMuestraColor_RELLENO.getGraphics());
        //Tercero, configuracion de transparencia 
        vSpinner_TRANSPARENCIA.setValue(Cnfg.CONFIG_INT_TRANSPARENCIA);
    }

